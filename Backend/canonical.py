# ─────────────────────────────────────────────────────────────────────────────
# Source Classification
# ─────────────────────────────────────────────────────────────────────────────

class ContentType(str, Enum):
    EMAIL = "email"
    MEETING_NOTES = "meeting_notes"
    CALENDAR_EVENT = "calendar_event"
    INSTANT_MESSAGE = "instant_message"
    DOCUMENT = "document"
    SPREADSHEET = "spreadsheet"
    PRESENTATION = "presentation"
    NOTE = "note"
    TASK = "task"
    COMMENT = "comment"
    VOICE_TRANSCRIPT = "voice_transcript"
    UNKNOWN = "unknown"


class ConnectorSource(str, Enum):
    OUTLOOK = "outlook"
    GMAIL = "gmail"
    TEAMS = "teams"
    SLACK = "slack"
    NOTION = "notion"
    GOOGLE_DRIVE = "google_drive"
    LOCAL_UPLOAD = "local_upload"
    SHAREPOINT = "sharepoint"
    WHATSAPP = "whatsapp"        # Future
    ZOOM = "zoom"                # Future


class ProcessingStatus(str, Enum):
    PENDING = "pending"
    PROCESSING = "processing"
    EMBEDDED = "embedded"
    FAILED = "failed"
    SKIPPED = "skipped"


# ─────────────────────────────────────────────────────────────────────────────
# Participant
# ─────────────────────────────────────────────────────────────────────────────

@dataclass
class Participant:
    """A person involved in a piece of content."""
    email: Optional[str] = None
    display_name: Optional[str] = None
    role: str = "participant"                    # "sender", "recipient", "cc", "attendee"
    person_id: Optional[UUID] = None             # Resolved entity ID (populated after entity matching)
    company_id: Optional[UUID] = None            # Resolved company


# ─────────────────────────────────────────────────────────────────────────────
# Attachment Reference
# ─────────────────────────────────────────────────────────────────────────────

@dataclass
class AttachmentRef:
    """Reference to a file attached to a piece of content."""
    filename: str
    mime_type: Optional[str] = None
    size_bytes: Optional[int] = None
    storage_path: Optional[str] = None          # MinIO path after upload
    is_processed: bool = False


# ─────────────────────────────────────────────────────────────────────────────
# Extracted Signals
# ─────────────────────────────────────────────────────────────────────────────

@dataclass
class ExtractedSignal:
    """
    A signal extracted from content by the NLP pipeline.
    Signals become commitments, risks, or decisions in the entity layer.
    """
    signal_type: str                             # "commitment", "risk", "decision", "action_item"
    text: str                                    # The extracted text
    confidence: float                            # 0.0–1.0
    owner_email: Optional[str] = None
    deadline_text: Optional[str] = None         # Raw text, e.g., "by Friday"
    deadline_parsed: Optional[datetime] = None  # Parsed datetime if resolvable


# ─────────────────────────────────────────────────────────────────────────────
# CANONICAL DOCUMENT — The Universal Internal Format
# ─────────────────────────────────────────────────────────────────────────────

@dataclass
class CanonicalDocument:
    """
    The single internal representation for all ingested content.

    Every connector produces this format.
    Every pipeline stage consumes this format.
    The LLM never sees the raw source format.

    Sources → CanonicalDocument → Entity Extraction → Storage → Embedding

    Field conventions:
      - id: system-generated UUID
      - source_id: original ID from the external system
      - content_type: what kind of content this is
      - connector_source: where it came from
      - occurred_at: when it happened (not when it was ingested)
      - ingested_at: when the system received it
      - title: human-readable title or subject
      - body_text: full text content (cleaned)
      - body_summary: short summary (generated by pipeline)
      - participants: all people involved
      - attachments: any files referenced
      - extracted_signals: NLP-detected commitments/risks/decisions
      - entity_ids: resolved entity IDs after matching
      - meta: flexible bag for source-specific metadata
    """

    # System identity
    id: UUID = field(default_factory=uuid4)
    ingested_at: datetime = field(default_factory=datetime.utcnow)

    # Source identity
    source_id: str = ""                          # External system's ID
    connector_source: ConnectorSource = ConnectorSource.LOCAL_UPLOAD
    content_type: ContentType = ContentType.UNKNOWN
    source_url: Optional[str] = None             # Link back to original

    # Content
    title: Optional[str] = None                 # Subject line, filename, meeting title
    body_text: str = ""                          # Full cleaned text
    body_summary: Optional[str] = None           # Short summary (max 500 chars)
    language: str = "en"

    # Timing
    occurred_at: Optional[datetime] = None      # When this happened in the real world
    thread_id: Optional[str] = None             # Email thread, Slack thread, etc.

    # Participants
    participants: list[Participant] = field(default_factory=list)

    # Attachments
    attachments: list[AttachmentRef] = field(default_factory=list)

    # Extracted Intelligence
    extracted_signals: list[ExtractedSignal] = field(default_factory=list)

    # Resolved Entity Links (populated after entity matching)
    person_ids: list[UUID] = field(default_factory=list)
    company_ids: list[UUID] = field(default_factory=list)
    project_ids: list[UUID] = field(default_factory=list)

    # Processing State
    processing_status: ProcessingStatus = ProcessingStatus.PENDING
    embedding_ids: list[str] = field(default_factory=list)   # Qdrant point IDs
    chunk_count: int = 0
    processing_error: Optional[str] = None

    # Deduplication
    content_hash: Optional[str] = None          # SHA-256 of body_text

    # Flexible metadata (source-specific fields that don't fit the schema)
    meta: dict[str, Any] = field(default_factory=dict)

    def __post_init__(self):
        if self.body_text and not self.content_hash:
            self.content_hash = hashlib.sha256(
                self.body_text.encode("utf-8")
            ).hexdigest()

    @property
    def sender(self) -> Optional[Participant]:
        for p in self.participants:
            if p.role == "sender":
                return p
        return self.participants[0] if self.participants else None

    @property
    def recipients(self) -> list[Participant]:
        return [p for p in self.participants if p.role in ("recipient", "cc", "to")]

    @property
    def attendees(self) -> list[Participant]:
        return [p for p in self.participants if p.role == "attendee"]

    @property
    def commitment_signals(self) -> list[ExtractedSignal]:
        return [s for s in self.extracted_signals if s.signal_type == "commitment"]

    @property
    def risk_signals(self) -> list[ExtractedSignal]:
        return [s for s in self.extracted_signals if s.signal_type == "risk"]

    def to_chunk_payload(self, chunk_text: str, chunk_index: int) -> dict[str, Any]:
        """
        Serialize chunk metadata for Qdrant vector storage.
        This is the payload stored alongside each embedding.
        """
        return {
            "document_id": str(self.id),
            "source_id": self.source_id,
            "source_type": self.connector_source.value,
            "content_type": self.content_type.value,
            "title": self.title,
            "text": chunk_text,
            "chunk_index": chunk_index,
            "occurred_at": self.occurred_at.isoformat() if self.occurred_at else None,
            "person_ids": [str(pid) for pid in self.person_ids],
            "company_ids": [str(cid) for cid in self.company_ids],
            "project_ids": [str(pid) for pid in self.project_ids],
            "thread_id": self.thread_id,
        }

    def to_db_dict(self) -> dict[str, Any]:
        """Serialize to a flat dict for database storage."""
        return {
            "id": self.id,
            "source_id": self.source_id,
            "source_type": self.connector_source.value,
            "content_type": self.content_type.value,
            "title": self.title,
            "source_url": self.source_url,
            "occurred_at": self.occurred_at,
            "ingested_at": self.ingested_at,
            "body_summary": self.body_summary,
            "chunk_count": self.chunk_count,
            "processing_status": self.processing_status.value,
            "content_hash": self.content_hash,
            "meta": self.meta,
        }


