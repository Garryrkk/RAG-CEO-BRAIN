class OutlookNormalizer:
    """Converts raw Outlook Graph API email payload → CanonicalDocument."""

    def normalize(self, raw: dict[str, Any]) -> CanonicalDocument:
        participants = []

        sender = raw.get("from", {}).get("emailAddress", {})
        if sender:
            participants.append(Participant(
                email=sender.get("address"),
                display_name=sender.get("name"),
                role="sender",
            ))

        for recipient in raw.get("toRecipients", []):
            addr = recipient.get("emailAddress", {})
            participants.append(Participant(
                email=addr.get("address"),
                display_name=addr.get("name"),
                role="recipient",
            ))

        for cc in raw.get("ccRecipients", []):
            addr = cc.get("emailAddress", {})
            participants.append(Participant(
                email=addr.get("address"),
                display_name=addr.get("name"),
                role="cc",
            ))

        body = raw.get("body", {}).get("content", "")

        return CanonicalDocument(
            source_id=raw.get("id", ""),
            connector_source=ConnectorSource.OUTLOOK,
            content_type=ContentType.EMAIL,
            source_url=raw.get("webLink"),
            title=raw.get("subject"),
            body_text=self._clean_html(body),
            occurred_at=self._parse_date(raw.get("sentDateTime")),
            thread_id=raw.get("conversationId"),
            participants=participants,
            meta={
                "importance": raw.get("importance"),
                "has_attachments": raw.get("hasAttachments", False),
                "internet_message_id": raw.get("internetMessageId"),
            },
        )

    def _clean_html(self, html: str) -> str:
        import re
        clean = re.sub(r"<[^>]+>", " ", html)
        clean = re.sub(r"\s+", " ", clean).strip()
        return clean

    def _parse_date(self, date_str: Optional[str]) -> Optional[datetime]:
        if not date_str:
            return None
        from dateutil import parser
        try:
            return parser.parse(date_str)
        except Exception:
            return None


class SlackNormalizer:
    """Converts raw Slack message payload → CanonicalDocument."""

    def normalize(self, raw: dict[str, Any], channel_name: str = "") -> CanonicalDocument:
        participants = [
            Participant(
                email=None,
                display_name=raw.get("user", ""),
                role="sender",
            )
        ]

        ts = raw.get("ts", "")
        occurred_at = None
        if ts:
            try:
                occurred_at = datetime.fromtimestamp(float(ts))
            except Exception:
                pass

        return CanonicalDocument(
            source_id=ts,
            connector_source=ConnectorSource.SLACK,
            content_type=ContentType.INSTANT_MESSAGE,
            title=f"Slack: #{channel_name}",
            body_text=raw.get("text", ""),
            occurred_at=occurred_at,
            thread_id=raw.get("thread_ts"),
            participants=participants,
            meta={
                "channel": channel_name,
                "channel_id": raw.get("channel"),
                "reaction_count": len(raw.get("reactions", [])),
            },
        )


class NotionNormalizer:
    """Converts Notion page payload → CanonicalDocument."""

    def normalize(self, raw: dict[str, Any], blocks: list[dict]) -> CanonicalDocument:
        title = ""
        props = raw.get("properties", {})
        for prop_name, prop_val in props.items():
            if prop_val.get("type") == "title":
                texts = prop_val.get("title", [])
                title = "".join(t.get("plain_text", "") for t in texts)
                break

        body_lines = []
        for block in blocks:
            block_type = block.get("type", "")
            block_content = block.get(block_type, {})
            rich_text = block_content.get("rich_text", [])
            text = "".join(t.get("plain_text", "") for t in rich_text)
            if text.strip():
                body_lines.append(text)

        created_time = raw.get("created_time")
        occurred_at = None
        if created_time:
            from dateutil import parser
            try:
                occurred_at = parser.parse(created_time)
            except Exception:
                pass

        return CanonicalDocument(
            source_id=raw.get("id", ""),
            connector_source=ConnectorSource.NOTION,
            content_type=ContentType.NOTE,
            source_url=raw.get("url"),
            title=title,
            body_text="\n".join(body_lines),
            occurred_at=occurred_at,
            meta={
                "page_id": raw.get("id"),
                "last_edited": raw.get("last_edited_time"),
                "created_by": raw.get("created_by", {}).get("id"),
            },
        )


class TeamsNormalizer:
    """Converts Microsoft Teams message payload → CanonicalDocument."""

    def normalize(self, raw: dict[str, Any]) -> CanonicalDocument:
        sender_info = raw.get("from", {}).get("user", {})
        participants = [
            Participant(
                email=None,
                display_name=sender_info.get("displayName", ""),
                role="sender",
            )
        ]

        body = raw.get("body", {}).get("content", "")
        if raw.get("body", {}).get("contentType") == "html":
            import re
            body = re.sub(r"<[^>]+>", " ", body)
            body = re.sub(r"\s+", " ", body).strip()

        from dateutil import parser
        occurred_at = None
        created_at = raw.get("createdDateTime")
        if created_at:
            try:
                occurred_at = parser.parse(created_at)
            except Exception:
                pass

        return CanonicalDocument(
            source_id=raw.get("id", ""),
            connector_source=ConnectorSource.TEAMS,
            content_type=ContentType.INSTANT_MESSAGE,
            title=raw.get("subject") or "Teams Message",
            body_text=body,
            occurred_at=occurred_at,
            thread_id=raw.get("replyToId"),
            participants=participants,
            meta={
                "channel_identity": raw.get("channelIdentity"),
                "message_type": raw.get("messageType"),
                "importance": raw.get("importance"),
            },
        )


class LocalFileNormalizer:
    """Converts local file upload metadata + extracted text → CanonicalDocument."""

    def normalize(
        self,
        filename: str,
        extracted_text: str,
        mime_type: str,
        storage_path: str,
        file_size: int,
    ) -> CanonicalDocument:
        content_type = self._infer_content_type(mime_type, filename)

        return CanonicalDocument(
            source_id=storage_path,
            connector_source=ConnectorSource.LOCAL_UPLOAD,
            content_type=content_type,
            title=filename,
            body_text=extracted_text,
            occurred_at=datetime.utcnow(),
            attachments=[
                AttachmentRef(
                    filename=filename,
                    mime_type=mime_type,
                    size_bytes=file_size,
                    storage_path=storage_path,
                    is_processed=True,
                )
            ],
            meta={
                "mime_type": mime_type,
                "storage_path": storage_path,
                "file_size": file_size,
            },
        )

    def _infer_content_type(self, mime_type: str, filename: str) -> ContentType:
        ext = filename.rsplit(".", 1)[-1].lower() if "." in filename else ""
        mapping = {
            "pdf": ContentType.DOCUMENT,
            "docx": ContentType.DOCUMENT,
            "doc": ContentType.DOCUMENT,
            "xlsx": ContentType.SPREADSHEET,
            "xls": ContentType.SPREADSHEET,
            "pptx": ContentType.PRESENTATION,
            "ppt": ContentType.PRESENTATION,
            "txt": ContentType.NOTE,
            "md": ContentType.NOTE,
        }
        return mapping.get(ext, ContentType.DOCUMENT)
